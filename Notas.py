#Hallar el promedio de 3 notas
#Datos:
'''
N1 = nota uno
N2 = nota dos
N3 = nota 3
P = promedio
'''

n1 = float(input('Ingresar primera nota: '))
n2 = float(input('Ingresar segunda nota: '))
n3 = float(input('Ingresar tercera nota: '))

p=(n1+n2+n3)/3
#print('El promedio de estas notas es: '+str(p)+' puntos.')
print('El promedio de estas notas es: ',p,' puntos.')